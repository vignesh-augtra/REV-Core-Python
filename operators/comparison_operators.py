num1 = 10
num2 = 100

greater_than = num1 > num2
print("{0} > {1} = {2}".format(num1, num2, greater_than))

less_than = num1 < num2
print("{0} < {1} = {2}".format(num1, num2, less_than))

similar = num1 == num2
print("{0} == {1} = {2}".format(num1, num2, similar))

greater_than_or_equal = num1 >= num2
print("{0} >= {1} = {2}".format(num1, num2, greater_than_or_equal))

less_than_or_equal = num1 <= num2
print("{0} <= {1} = {2}".format(num1, num2, less_than_or_equal))

not_qual = num1 != num2
print("{0} != {1} = {2}".format(num1, num2, not_qual))